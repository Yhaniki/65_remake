        // Parsed once: Lit[row][bar] == true -> that bar shows the lit (002) frame on that row.
        private static bool[][] _lit;

        public static bool[][] Lit
        {
            get
            {
                if (_lit == null)
                {
                    var t = new bool[Rows][];
                    for (int r = 0; r < Rows; r++)
                    {
                        t[r] = new bool[Bars];
                        var s = RowStrings[r];
                        for (int c = 0; c < Bars && c < s.Length; c++) t[r][c] = s[c] == '1';
                    }
                    _lit = t;
                }
                return _lit;
            }
        }
